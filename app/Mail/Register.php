namespace App\Mail;

use Illuminate\Bus\Queueable;
use Illuminate\Contracts\Queue\ShouldQueue;
use Illuminate\Mail\Mailable;
use Illuminate\Queue\SerializesModels;
use DB;

class Register extends Mailable
{
    use Queueable, SerializesModels;

    /**
     * Create a new message instance.
     *
     * @return void
     */
    public function __construct($user_code)
    {
        $user = DB::table('users')->where('code', $user_code)->first();
        if (!$user) return redirect(route('homepage'));

        $this->name = $user->name;
    }

    /**
     * Build the message.
     *
     * @return $this
     */
    public function build()
    {
        return $this
            ->from('office@clevada.com', 'Clevada')
            ->subject('Your account has beeen created')
            ->markdown('emails.auth.register', [
                'name' => 'EEEE',
            ]);
    }
}
